let content = fs.readFileSync(FILE_PATH, 'utf8');
let changeCount = 0;

for (const [wrong, correct] of REPLACEMENTS) {
  // Ne pas modifier les URLs et certains contextes
  if (wrong.includes(' ')) {
    // Pour les expressions avec espaces, remplacement direct
    const regex = new RegExp(wrong.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g');
    const matches = content.match(regex);
    if (matches) {
      changeCount += matches.length;
      content = content.replace(regex, correct);
    }
  } else {
    // Pour les mots simples, utiliser les frontières de mot
    const regex = new RegExp(`\\b${wrong.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'g');
    const matches = content.match(regex);
    if (matches) {
      changeCount += matches.length;
      content = content.replace(regex, correct);
    }
  }
}

fs.writeFileSync(FILE_PATH, content, 'utf8');
